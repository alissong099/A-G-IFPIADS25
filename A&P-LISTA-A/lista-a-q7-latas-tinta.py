# 07. Escreva um programa que calcule quantas latas de tinta são necessárias para pintar uma área.

# entrada

área1 = 100
área2 = 50
área3 = 75

rendimento1 = 20
rendimento2 = 10
rendimento3 = 25

# processamento

latas1 = (área1 // rendimento1)
latas2 = (área2 // rendimento2)
latas3 = (área3 // rendimento3)

# saída

print(f'Para pintar uma área de {área1} m², serão necessárias {latas1} latas de tinta.')
print(f'Para pintar uma área de {área2} m², serão necessárias {latas2} latas de tinta.')
print(f'Para pintar uma área de {área3} m², serão necessárias {latas3} latas de tinta.')