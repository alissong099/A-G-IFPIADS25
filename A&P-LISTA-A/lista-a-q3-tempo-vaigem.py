# 03. Escreva um programa que calcule quanto tempo levará uma viagem, dado a distância e a
# velocidade média.

# entrada
distância1 = 300
distância2 = 150
distância3 = 600

velocidade1 = 100
velocidade2 = 50
velocidade3 = 120
# processamento
tempo1 = distância1 / velocidade1
tempo2 = distância2 / velocidade2
tempo3 = distância3 / velocidade3
# saída
print(f'O tempo estimado da viagem é de {tempo1} em horas.')
print(f'O tempo estimado da viagem é de {tempo2} em horas.')
print(f'O tempo estimado da viagem é de {tempo3} em horas.')