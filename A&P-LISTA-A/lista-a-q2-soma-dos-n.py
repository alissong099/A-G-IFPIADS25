# 02. Escreva um programa que calcule a soma dos primeiros N números naturais.

# entrada
N1 = 5
N2 = 10
N3 = 100

# processamento
soma1 = (N1 * (N1+1)) // 2
soma2 = (N2 * (N2+1)) // 2
soma3 = (N3 * (N3+1)) // 2
# saída
print(f'A soma dos primeiros {N1} números naturais é: {soma1}')
print(f'A soma dos primeiros {N2} números naturais é: {soma2}')
print(f'A soma dos primeiros {N3} números naturais é: {soma3}')