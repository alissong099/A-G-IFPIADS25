# 08. Escreva um programa que calcule o Índice de Massa Corporal (IMC), dado o peso e a altura.

# entrada

peso1 = 70
peso2 = 90
peso3 = 50

altura1 = 1.75
altura2 = 1.80
altura3 = 1.60

# processamento

imc1 = peso1 / (altura1 ** 2)
imc2 = peso2 / (altura2 ** 2)
imc3 = peso3 / (altura3 ** 2)

# saída

print(f'Seu IMC é: {imc1:.2f}')
print(f'Seu IMC é: {imc2:.2f}')
print(f'Seu IMC é: {imc3:.2f}')